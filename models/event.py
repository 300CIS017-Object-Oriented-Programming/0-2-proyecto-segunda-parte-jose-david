class Event:
    def __init__(self, name, date, opening_time, show_time, location, address, city, artists):
        self.type = ''
        self.name = name
        self.date = date
        self.opening_time = opening_time
        self.show_time = show_time
        self.location = location
        self.address = address
        self.city = city
        self.artists = artists


